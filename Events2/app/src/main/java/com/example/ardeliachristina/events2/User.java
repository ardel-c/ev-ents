package com.example.ardeliachristina.events2;

public class User {


    String fullname, username, password, phoneNumber, address, gender, userID;

    public User(String fullname, String username, String password, String phoneNumber, String address, String gender, String userID) {
        this.fullname = fullname;
        this.username = username;
        this.password = password;
        this.phoneNumber = phoneNumber;
        this.address = address;
        this.gender = gender;
        this.userID= userID;
    }

}
